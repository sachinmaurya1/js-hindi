// switch case 

const month  = 3
switch (month){
    case 1:
        console.log("january")
        break;
    case 2:
        console.log("feb")
        break;
    case 3:
        console.log("march")
        break;
    case 1:
        console.log("april")
        break;
    default:
        console.log("default case match");
        break;
   
}